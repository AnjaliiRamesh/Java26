package Array2D;

public class basics {
    public static void main(String[] args) {
        int [][] a = {
            {6,2,7},
            {0,-3, 1}

        };

        System.out.println(a[0][0]);
        System.out.println(a[0][1]);
        System.out.println(a[0][2]);

        System.out.println(a[1][0]);
        System.out.println(a[1][1]);
        System.out.println(a[1][2]);
    }
}
