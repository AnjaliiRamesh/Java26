public class unBoxing {
    public static void main(String[] args) {
        Integer a = 100;
        Double d = 90.0096;
        Character c = 'h';
        
        //unboxing

        int x=a;
        double y = d;
        char z = c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

    }
    
}
